## The functions below return the inverse for an invertible matrix
## If an inverse, has already been calculated once, it will be
## returned from the cache later on

## Function creating a special matrix with caching properties

makeCacheMatrix <- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) i <<- inverse
  getinverse <- function() i
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


## Function returning the inversion of a matrix created with makeCacheMatrix
## This function starts with checking for a cached version of x inversion
## If a cached version doesn't exist, it will be created

cacheSolve <- function(x, ...) {
  ## Return a matrix that is the inverse of 'x'
  i <- x$getinverse()
  if(!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)
  x$setinverse(i)
  i
}
